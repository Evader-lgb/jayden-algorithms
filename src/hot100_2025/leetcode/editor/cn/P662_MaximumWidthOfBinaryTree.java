/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int widthOfBinaryTree(TreeNode root) {
        if(root == null){
            return 0;
        }

        int maxWidth = 0;
        Queue<Pair<TreeNode,Integer>> queue = new LinkedList<>();
        queue.offer(new Pair<>(root,0));

        while (!queue.isEmpty()){
            int size = queue.size();
            int left = queue.peek().getValue();
            int right = left;

            for (int i = 0; i < size; i++) {
                Pair<TreeNode, Integer> currNode = queue.poll();
                TreeNode key = currNode.getKey();
                right = currNode.getValue();

                if (key.left != null){
                    queue.offer(new Pair<>(key.left,2 * right));
                }

                if (key.right != null){
                    queue.offer(new Pair<>(key.right,2 * right + 1));
                }

            }

            int currWidth = right - left +1;
            maxWidth= Math.max(currWidth,maxWidth);
        }

        return maxWidth;
    }

    class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;
        TreeNode() {}
        TreeNode(int val) { this.val = val; }
        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }

    // 辅助类：用于存储键值对
    class Pair<K, V> {
        private K key;
        private V value;

        public Pair(K key, V value) {
            this.key = key;
            this.value = value;
        }

        public K getKey() {
            return key;
        }

        public V getValue() {
            return value;
        }
    }
}
